            라. 회원의 포상과 징계
            마. 이사회가 위임한 사항
            바. 기타 회장이 중요하다고 인정하여 부의한 사항

            제 16 조(분과위원회)
            ①본회의 주요현안에 대해 회장을 보좌하고 조직의 활성화를 위해 분과위원회를 둔다.
            ②분과위원회 위원장은 회장이 임명한다.
            ③분과위원회는 회장의 필요에 따라 증감하고 분장업무를 조정할 수 있다.
            
            <h3>제 5 장 재정</h3>
            제 17 조(재정)
            ①본회의 재정은 세입세출 예산에 따라 집행한다.
            ②본회의 운영을 위한 재원은 다음 각 호의 구분으로 한다.
            가. 회비(평생회비 포함) 및 입회비
            나. 임원분담금
            다. 찬조금 및 특별희사금
            라. 전기이월금(기금)
            마. 기타 수입금

            제 18 조(회계연도) 본회의 회계연도는 매년 4월 1일부터 익년 3월 31일까지로 한다.

            제 19 조(회계보고) 상근부회장은 당해연도 회계수지를 결산하여 감사를 받은 후 운영위원회에 보고하고, 매 2년마다 개최되는 정기총회의 승인을 받아야 한다.
            
